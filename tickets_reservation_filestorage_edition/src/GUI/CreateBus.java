    private void calculateFarePerKM() {
        try {
            int FPKM = Integer.parseInt(farePerKM.getText());
            int D = Integer.parseInt(distance.getText());
            show_validation_here.setText(" ");
            fare.setText(String.valueOf(FPKM * D));
        } catch (NumberFormatException e) {
            show_validation_here.setText("Invalid Number");
            e.getMessage();
        }
    }

    private void saveBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveBtnMouseClicked
        // TODO add your handling code here:
        boolean valid = true;
        if (originDropMenu.getSelectedItem().toString().equals("-- Select Origin --")
                || destinationDropMenu.getSelectedItem().toString().equals("-- Destination --")
                || distance.getText().equals("--")) {
            valid = false;
            JOptionPane.showMessageDialog(null, "Pick an origin and destination.");
        }
        if (fare.getText().equals("--")) {
            valid = false;
            JOptionPane.showMessageDialog(null, "Enter a fare/km and press enter");
        }
        if (timetableMenu.getModel().getSize() == 0) {
            valid = false;
            JOptionPane.showMessageDialog(null, "At least one time is required.");
        }

        if (valid) {
            try {
                String[] data = new String[6 + timetableMenu.getModel().getSize()];
                data[0] = String.valueOf((Integer.parseInt(FileStorage.getLastId("buses")) + 1));
                data[1] = originDropMenu.getSelectedItem().toString();
                data[2] = destinationDropMenu.getSelectedItem().toString();
                data[3] = distance.getText();
                data[4] = fare.getText();
                data[5] = seatsDropMenu.getSelectedItem().toString();
                for (int i = 6; i < data.length; i++) {
                    data[i] = (String) jListModel.getElementAt(i - 6);
                }
                if (edit.equals("true")) {
                    data[0] = busID.getText();
                    FileStorage.updateRecord("buses", data[0], data);
                    JOptionPane.showMessageDialog(null, "Bus Updated");
                } else {
                    FileStorage.addRecord("buses", data);
                    JOptionPane.showMessageDialog(null, "Bus Created");
                }
                dispose();
            } catch (Exception e) {
            }

        }
    }//GEN-LAST:event_saveBtnMouseClicked

    private void formWindowOpened(WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        timetableMenu.setModel(jListModel);
        ArrayList<String> Origin = new ArrayList<>();
        try {
            ArrayList<String[]> rows = FileStorage.readData("cities");
            for (String[] row : rows) {
                if (!Origin.contains(row[1])) {
                    Origin.add(row[1]);
                }
            }
        } catch (Exception e) {
        }
        String[] originArr = new String[Origin.size()];
        originArr = Origin.toArray(originArr);

        originArr[0] = "-- Select Origin --";
        DefaultComboBoxModel originModel = new DefaultComboBoxModel(originArr);
        originDropMenu.setModel(originModel);
        if (edit.equals("true")) {
            setTitle("Bus Ticket Reservation - Edit Bus");
            jLabel10.setVisible(true);
            searchBusID.setVisible(true);
            searchBtn.setVisible(true);
            deleteBusBtn.setVisible(true);
        } else {
            try {
                Integer ID = (Integer.parseInt(FileStorage.getLastId("buses")) + 1);
                busID.setText(String.valueOf(ID));
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_formWindowOpened

