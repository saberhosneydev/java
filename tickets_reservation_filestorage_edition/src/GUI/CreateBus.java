    private void originDropMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_originDropMenuActionPerformed
        // TODO add your handling code here:
        ArrayList<String> Destination = new ArrayList<>();
        Destination.add("-- Destination --");
        try {
            ArrayList<String[]> rows = FileStorage.readData("cities");
            for (String[] row : rows) {
                if (originDropMenu.getSelectedItem().toString().equals((row[1]))) {
                    Destination.add(row[2]);
                }
            }
        } catch (Exception e) {
        }
        String[] destArr = new String[Destination.size()];
        destArr = Destination.toArray(destArr);
        DefaultComboBoxModel destModel = new DefaultComboBoxModel(destArr);
        destinationDropMenu.setModel(destModel);
    }//GEN-LAST:event_originDropMenuActionPerformed

