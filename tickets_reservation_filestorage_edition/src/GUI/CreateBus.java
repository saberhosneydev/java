    private void searchBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchBtnMouseClicked
        if (!searchBusID.getText().equals("")) {
            try {
                ArrayList<String[]> buses = FileStorage.readData("buses");
                boolean found = false;
                for (String[] bus : buses) {
                    if (bus[0].equals(searchBusID.getText())) {
                        busID.setText(bus[0]);
                        originDropMenu.setSelectedItem(bus[1]);
                        destinationDropMenu.setSelectedItem(bus[2]);
                        distance.setText(bus[3]);
                        fare.setText(bus[4]);
                        farePerKM.setText(String.valueOf(Integer.parseInt(fare.getText()) / Integer.parseInt(distance.getText())));
                        seatsDropMenu.setSelectedItem(bus[5]);
                        jListModel.removeAllElements();
                        for (int i = 6; i < bus.length; i++) {
                            jListModel.addElement(bus[i]);
                        }
                        found = true;
                    }
                }
                if (!found) {
                    JOptionPane.showMessageDialog(null, "No bus with this ID found");
                }
            } catch (Exception e) {
            }
        } else JOptionPane.showMessageDialog(null, "Can't be empty");
    }//GEN-LAST:event_searchBtnMouseClicked

    private void deleteBusBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteBusBtnMouseClicked
        if (!searchBusID.getText().equals("") && !busID.getText().equals("--")) {
            try {
                String id = busID.getText();
                int x = JOptionPane.showConfirmDialog(null, "Confirm and delete ?", "Delete bus", JOptionPane.YES_NO_OPTION);
                if (x == JOptionPane.YES_OPTION) {
                    FileStorage.deleteRecord("buses", id);
                    JOptionPane.showMessageDialog(null, "Bus NO.( " + id + " ) deleted.");
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Nothing deleted");
                }
            } catch (Exception e) {
            }
        } else {
            JOptionPane.showMessageDialog(null, "Press Search first");
        }
    }//GEN-LAST:event_deleteBusBtnMouseClicked

