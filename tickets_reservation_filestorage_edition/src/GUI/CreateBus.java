    private void destinationDropMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_destinationDropMenuActionPerformed
        // TODO add your handling code here:
        try {
            ArrayList<String[]> rows = FileStorage.readData("cities");
            for (String[] row : rows) {
                if (destinationDropMenu.getSelectedItem().toString().equals((row[2])) && originDropMenu.getSelectedItem().toString().equals(row[1])) {
                    distance.setText(row[3]);
                    if (edit.equals("true")) {
                        int FPKM = Integer.parseInt(farePerKM.getText());
                        int D = Integer.parseInt(distance.getText());
                        fare.setText(String.valueOf(D * FPKM));
                    }
                }
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_destinationDropMenuActionPerformed

